// ── Language Engine ──────────────────────────────────────────
const LanguageManager = {

  current: 'en',

  init() {
    // Load saved language preference
    this.current = localStorage.getItem('health_app_lang') || this.detectLanguage();
    this.apply(this.current);
    this.renderSwitcher();
  },

  detectLanguage() {
    // Auto-detect based on browser language
    const lang = navigator.language || navigator.userLanguage || 'en';
    if (lang.startsWith('ta')) return 'ta';
    if (lang.startsWith('hi')) return 'hi';
    return 'en';
  },

  apply(langCode) {
    if (!TRANSLATIONS[langCode]) return;
    this.current = langCode;
    localStorage.setItem('health_app_lang', langCode);

    const t = TRANSLATIONS[langCode];
    document.documentElement.lang = langCode;
    document.documentElement.dir = t.dir || 'ltr';

    // Apply all translations to elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (t[key]) {
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
          el.placeholder = t[key];
        } else {
          el.textContent = t[key];
        }
      }
    });

    // Apply to placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      if (t[key]) el.placeholder = t[key];
    });

    // Update switcher active state
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.lang === langCode);
    });
  },

  renderSwitcher() {
    const container = document.getElementById('language-switcher');
    if (!container) return;

    container.innerHTML = Object.entries(TRANSLATIONS).map(([code, lang]) => `
      <button
        class="lang-btn ${code === this.current ? 'active' : ''}"
        data-lang="${code}"
        onclick="LanguageManager.apply('${code}')"
        title="Switch to ${lang.name}"
      >
        ${lang.flag} ${lang.name}
      </button>
    `).join('');
  },

  t(key) {
    return TRANSLATIONS[this.current][key] || TRANSLATIONS['en'][key] || key;
  }
};

// Auto-initialize when DOM is ready
document.addEventListener('DOMContentLoaded', () => LanguageManager.init());